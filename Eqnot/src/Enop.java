/*A Java program to find 2 numbers equal or not*/
class Enop 
{
	public static void main(String[] args) 
	{
		int n1=10;
		int n2=20;
		System.out.println("The First number is:"+n1);
		System.out.println("The Second numberr is:"+n2);
		if(n1==n2)//using equal operator
		{
			System.out.println("The numbers are equal");
		}
		else if(n1!=n2)//using not equal operator
		{
			System.out.println("The numbers are not equal");
		}
	}
}
